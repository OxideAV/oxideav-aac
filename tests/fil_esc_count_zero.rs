//! Regression: ADTS-AAC frames whose trailing FIL element advertises
//! `count == 15` and `esc_count == 0` used to trip
//! `Error::invalid("bitreader: out of bits")` because we read one byte
//! more than the spec defines.
//!
//! ISO/IEC 14496-3:2009 Table 4.11 — `fill_element()`:
//!
//! ```text
//! cnt = count;                  // 4 bits
//! if (cnt == 15)
//!     cnt += esc_count - 1;     // 8 bits
//! while (cnt > 0)
//!     cnt -= extension_payload(cnt);
//! ```
//!
//! With `count == 15` and `esc_count == 0` the spec yields
//! `cnt = 15 + 0 - 1 = 14` payload bytes — i.e. the escape byte costs
//! one byte from the original 15-byte budget, leaving 14 to fill. The
//! earlier `count += esc_count.saturating_sub(1)` form clamped `0 - 1`
//! to `0` and read 15 bytes instead, overrunning the bitreader on the
//! last frame of an ADTS packet.
//!
//! The fixture is the third raw ADTS frame of the public sample
//! `congress_mtgox_coins.mp4`'s AAC track (44.1 kHz / 2 ch / LC profile,
//! 371-byte ADTS frame). 51 of its 3711 packets carry the same FIL
//! tail — fixing this single arithmetic line makes all 51 decode.

