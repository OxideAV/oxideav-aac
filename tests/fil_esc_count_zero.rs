#[test]
fn fil_count_15_esc_count_0_decodes() {
    // Header is 0xFF F1 50 80 ... → MPEG-4, no-CRC, profile=LC,
    // sample-rate-idx=4 (44.1 kHz), channel-config=2 (stereo). The AAC
    // decoder picks these up from the ADTS header, so seed
    // CodecParameters with matching values for clarity only.
    let mut params = CodecParameters::audio(CodecId::new("aac"));
    params.sample_rate = Some(44_100);
    params.channels = Some(2);
    let mut dec = oxideav_aac::decoder::make_decoder(&params).expect("make decoder");

    let pkt = Packet {
        stream_index: 0,
        time_base: TimeBase::new(1, 44_100),
        pts: Some(0),
        dts: Some(0),
        duration: Some(1024),
        flags: Default::default(),
        data: PACKET_2_FIL_ESC_ZERO.to_vec(),
    };
    dec.send_packet(&pkt).expect("send_packet");
    let frame = dec
        .receive_frame()
        .expect("receive_frame must not error on conformant FIL with esc_count=0");
    match frame {
        Frame::Audio(a) => {
            // AAC-LC at 44.1 kHz emits 1024 samples per frame (no SBR
            // doubling — the FIL extension here is a non-SBR fill).
            assert_eq!(a.samples, 1024, "expected 1024 samples (1 AAC-LC frame)");
            assert_eq!(a.data.len(), 1, "interleaved S16 emits a single plane");
            // 1024 samples × 2 channels × 2 bytes/sample.
            assert_eq!(
                a.data[0].len(),
                1024 * 2 * 2,
                "interleaved stereo S16 should be 4096 bytes"
            );
        }
        _ => panic!("expected audio frame"),
    }
}
